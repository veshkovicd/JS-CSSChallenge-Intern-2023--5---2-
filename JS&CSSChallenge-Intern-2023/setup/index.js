// create a card element with the given data
function createCard(cardData) {
    // create the card element
    const card = document.createElement("div");
    card.classList.add("card");



    // add the profile image
    const profileImage = document.createElement("img");
    profileImage.src = cardData.profile_image;
    profileImage.classList.add("profile-image");
    card.appendChild(profileImage);

    // add the profile name
    const profileName = document.createElement("p");
    profileName.textContent = cardData.name;
    profileName.classList.add("profile-name");
    card.appendChild(profileName);

    // add the source icon
    const sourceIcon = document.createElement("img");
    let iconPath = '';
    if(cardData.source_type === "facebook") {
     
         iconPath = cardData.source_type === "C:\Users\User\Desktop\JS&CSSChallenge-Intern-2023 (5) (2)\JS&CSSChallenge-Intern-2023\icons\facebook.svg";
    }
    if(cardData.source_type === "instagram") {
      
         iconPath = cardData.source_type === "C:\Users\User\Desktop\JS&CSSChallenge-Intern-2023 (5) (2)\JS&CSSChallenge-Intern-2023\icons\instagram-logo.svg";
    }

    sourceIcon.src = iconPath;
    sourceIcon.classList.add("source-icon");
    card.appendChild(sourceIcon);

    // add the card image
    const img = document.createElement("img");
    img.src = cardData.image;
    card.appendChild(img);

    // add the card caption
    const caption = document.createElement("p");
    caption.textContent = cardData.caption;
    card.appendChild(caption);

    // add the card type
    const type = document.createElement("p");
    type.textContent = cardData.type;
    card.appendChild(type);

    // add the card source link
    const sourceLink = document.createElement("a");
    sourceLink.href = cardData.source_link;
    sourceLink.textContent = cardData.source_type;
    card.appendChild(sourceLink);

    // add the card date
    const date = document.createElement("p");
    date.textContent = cardData.date;
    card.appendChild(date);


    // add the card likes counter and heart icon
    const likes = document.createElement("div");
    likes.classList.add("likes");

    const likesCounter = document.createElement("p");
    likesCounter.textContent = cardData.likes;
    likes.appendChild(likesCounter);

    card.appendChild(likes);


    return card.outerHTML;
}

